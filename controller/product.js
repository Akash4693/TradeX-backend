const express = require("express");
const { isSeller, isAuthenticated, isAdmin } = require("../middleware/auth");
const catchAsyncErrors = require("../middleware/catchAsyncErrors");
const router = express.Router();
const Product = require("../model/product");
const Order = require("../model/order");
const Shop = require("../model/shop");
const cloudinary = require("cloudinary");
const ErrorHandler = require("../utils/ErrorHandler");

// create product
router.post(
  "/create-product",
  catchAsyncErrors(async (req, res, next) => {
    try {
      const { shopId, images } = req.body;

      // Validate Shop
      const shop = await Shop.findById(shopId);
      if (!shop) {
        return next(new ErrorHandler("Shop ID is invalid!", 400));
      }

      // Ensure images is an array
      let imagesArray = [];
      if (typeof images === "string") {
        imagesArray.push(images);
      } else {
        imagesArray = images;
      }

      // Upload Images to Cloudinary (Parallel Processing)
      const uploadPromises = imagesArray.map((image) =>
        cloudinary.v2.uploader.upload(image, { folder: "products" })
      );

      const uploadedImages = await Promise.all(uploadPromises);

      // Extract Image URLs
      const imagesLinks = uploadedImages.map(({ public_id, secure_url }) => ({
        public_id,
        url: secure_url,
      }));

      // Create Product
      const productData = {
        ...req.body,
        images: imagesLinks,
        shop,
      };

      const product = await Product.create(productData);

      res.status(201).json({
        success: true,
        product,
      });
    } catch (error) {
      return next(new ErrorHandler(error.message, 400));
    }
  })
);

