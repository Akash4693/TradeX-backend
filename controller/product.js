// delete product of a shop

router.delete(
  "/delete-shop-product/:id",
  isSeller,
  catchAsyncErrors(async (req, res, next) => {
    try {
      const product = await Product.findById(req.params.id);

      if (!product) {
        return next(new ErrorHandler("Product not found with this id", 404));
      }

      // Use Promise.all to delete all images concurrently
      const imageDeletionPromises = product.images.map((image) =>
        cloudinary.v2.uploader.destroy(image.public_id)
      );

      await Promise.all(imageDeletionPromises); // Wait for all deletions to complete

      const deleteResult = await Product.deleteOne({ _id: req.params.id });

      // Check if deletion was successful
      if (deleteResult.deletedCount === 0) {
        return next(new ErrorHandler("Failed to delete the product", 400));
      }

      console.log("Product removed successfully from the database.");
      
      res.status(200).json({
        success: true,
        message: "Product deleted successfully!",
      });
    } catch (error) {
      next(new ErrorHandler(error.message || "Error deleting product", 400));
    }
  })
);


